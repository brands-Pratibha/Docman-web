// Helper to find a blog by ID
function getBlogById(id) {
    return blogPosts.find(post => post.id === parseInt(id));
}

// Helper to get related posts (e.g. same category or just random others)
function getRelatedPosts(currentId, category) {
    return blogPosts
        .filter(post => post.id !== parseInt(currentId))
        .sort((a, b) => (a.category === category ? -1 : 1))
        .slice(0, 3);
}
